//
//    Output, int S_INDEX_LAST_C, the index in S of the last occurrence of the character,
//    or -1 if it does not occur.
//
{
  int n;
  const char *t;

  n = strlen ( s ) - 1;
  t = s + strlen ( s ) - 1;

  while ( 0 <= n ) 
  {
    if ( *t == c )
    {
      return n;
    }
    t--;
    n--;
  }

  return (-1);
}
